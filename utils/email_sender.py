"""
Email Sending Utilities
Handles email functionality using various email services.
"""

import logging
import re
from datetime import datetime
from config.settings import Settings

logger = logging.getLogger(__name__)


class EmailSender:
    """Email sender using Resend API."""

    def __init__(self, api_key=None, from_email=None):
        """Initialize email sender."""
        self.api_key = api_key or Settings.RESEND_API_KEY
        self.from_email = from_email or Settings.EMAIL_FROM

        if not self.api_key:
            raise ValueError("Resend API key not configured")
        if not self.from_email:
            raise ValueError("From email not configured")

    def send(self, to_email, subject, content, content_type="markdown"):
        """
        Send an email.

        Args:
            to_email: Recipient email address
            subject: Email subject line
            content: Email content (markdown or html)
            content_type: "markdown" or "html" (default: markdown)

        Returns:
            True if sent successfully, False otherwise
        """
        try:
            import resend
            resend.api_key = self.api_key

            # Convert markdown to HTML if needed
            if content_type == "markdown":
                html_content = self._create_html_email(content, subject)
            else:
                html_content = content

            params = {
                "from": self.from_email,
                "to": [to_email],
                "subject": subject,
                "html": html_content
            }

            response = resend.Emails.send(params)
            logger.info("Email sent! (ID: %s)", response['id'])
            return True

        except Exception as e:
            logger.error("Email error: %s", e)
            return False

